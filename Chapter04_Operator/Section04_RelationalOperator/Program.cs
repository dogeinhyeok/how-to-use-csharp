/* 관계 연산자 */
Console.WriteLine($"3 > 4 : {3 > 4}");      // 3 > 4 : False
Console.WriteLine($"3 >= 4 : {3 >= 4}");    // 3 >= 4 : False
Console.WriteLine($"3 < 4 : {3 < 4}");      // 3 < 4 : True
Console.WriteLine($"3 <= 4 : {3 <= 4}");    // 3 <= 4 : True
Console.WriteLine($"3 == 4 : {3 == 4}");    // 3 == 4 : False
Console.WriteLine($"3 != 4 : {3 != 4}");    // 3 != 4 : True